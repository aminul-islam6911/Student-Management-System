    private void createActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createActionPerformed
        
        if(txtuser.getText().length() == 0)
        {
            JOptionPane.showMessageDialog(this,"Type Username");
        }
        else if(txtpass.getText().length() == 0)
        {
            JOptionPane.showMessageDialog(this,"Type Password");
        }
        else if(txtpass.getText().equals(txtconfirm.getText())== false)
        {
            JOptionPane.showMessageDialog(this,"Password Not Matched");
        }
        else
        {
            try {
                
                String username = txtuser.getText();
                String confirmpass = txtconfirm.getText();
                
                Class.forName("com.mysql.jdbc.Driver");
                con = DriverManager.getConnection("jdbc:mysql://localhost/stdmanagement","root","");
                pst = con.prepareStatement("insert into user(username,password)values(?,?)");
                pst.setString(1, username);
                pst.setString(2, confirmpass);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this,"New User Added");
                txtuser.setText("");
                txtpass.setText("");
                txtconfirm.setText("");
                txtuser.requestFocus();
                
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Newuser.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(Newuser.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_createActionPerformed

